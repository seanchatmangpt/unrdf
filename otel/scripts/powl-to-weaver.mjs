#!/usr/bin/env node
/**
 * powl-to-weaver — Deterministic POWL AST → OTel Weaver registry YAML generator
 *
 * Walks a POWL AST and derives OTel span definitions for each node, then writes
 * a weaver-compatible registry YAML to otel/registry/powl-{processId}.yaml.
 *
 * Span naming convention:
 *   activity      → {processId}.{snake_label}
 *   xor           → {processId}.choice.{branch_idx}
 *   loop          → {processId}.loop.do / {processId}.loop.redo
 *   partial_order → {processId}.concurrent.{i}
 *
 * After writing, runs: weaver registry check --registry otel/registry/
 *
 * Usage:
 *   node otel/scripts/powl-to-weaver.mjs --powl-ast artifacts/powl/hotel/ast.json
 *   node otel/scripts/powl-to-weaver.mjs --powl-ast-dir artifacts/powl/  # batch
 *   POWL_AST_PATH=artifacts/powl/hotel/ast.json node otel/scripts/powl-to-weaver.mjs
 */

import { execSync } from 'node:child_process'
import { existsSync, mkdirSync, readFileSync, readdirSync, writeFileSync } from 'node:fs'
import { dirname, join, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const DIR = dirname(dirname(fileURLToPath(import.meta.url)))  // otel/ root

// ── Span derivation ─────────────────────────────────────────────────────────

/**
 * Walk POWL AST and collect span definitions.
 * @param {object} node — POWL AST node
 * @param {string} processId — slug
 * @param {string} parentSpanName — for linking
 * @param {object[]} spans — accumulator
 * @returns {object[]} span definitions
 */
function collectSpans(node, processId, parentSpanName, spans = []) {
  const toSnake = s => s.toLowerCase().replace(/[\s\-]+/g, '_').replace(/[^a-z0-9_]/g, '')

  switch (node.type) {
    case 'activity':
    case 'agent_task':
    case 'human_task':
    case 'tool_task': {
      const spanName = `${processId}.${toSnake(node.label)}`
      spans.push({
        spanName,
        nodeType: node.type,
        activityLabel: node.label,
        nodeId: node.id,
        parentSpanName: parentSpanName ?? null,
      })
      break
    }

    case 'silent':
      // No span for silent transitions
      break

    case 'xor': {
      for (let i = 0; i < node.children.length; i++) {
        const branchSpan = `${processId}.choice.${i}`
        spans.push({
          spanName: branchSpan,
          nodeType: 'xor_branch',
          nodeId: `${node.id}_branch_${i}`,
          parentSpanName: parentSpanName ?? null,
        })
        collectSpans(node.children[i], processId, branchSpan, spans)
      }
      break
    }

    case 'loop': {
      const doSpan = `${processId}.loop.do`
      spans.push({
        spanName: doSpan,
        nodeType: 'loop_do',
        nodeId: `${node.id}_do`,
        parentSpanName: parentSpanName ?? null,
      })
      collectSpans(node.doBody, processId, doSpan, spans)

      if (node.redoBody) {
        const redoSpan = `${processId}.loop.redo`
        spans.push({
          spanName: redoSpan,
          nodeType: 'loop_redo',
          nodeId: `${node.id}_redo`,
          parentSpanName: parentSpanName ?? null,
        })
        collectSpans(node.redoBody, processId, redoSpan, spans)
      }
      break
    }

    case 'partial_order': {
      for (let i = 0; i < node.children.length; i++) {
        const concSpan = `${processId}.concurrent.${i}`
        spans.push({
          spanName: concSpan,
          nodeType: 'concurrent_branch',
          nodeId: `${node.id}_conc_${i}`,
          parentSpanName: parentSpanName ?? null,
        })
        collectSpans(node.children[i], processId, concSpan, spans)
      }
      break
    }
  }

  return spans
}

// ── YAML serialization ──────────────────────────────────────────────────────

function spanToYaml(span, indent = '  ') {
  const lines = [
    `${indent}- id: span.${span.spanName}`,
    `${indent}  type: span`,
    `${indent}  span_kind: internal`,
    `${indent}  brief: "POWL ${span.nodeType.replace(/_/g, ' ')} span for ${span.activityLabel ?? span.spanName}"`,
    `${indent}  stability: experimental`,
    `${indent}  attributes:`,
    `${indent}    - ref: powl.model.slug`,
    `${indent}    - ref: powl.model.fitness`,
    `${indent}    - ref: powl.error_category`,
    `${indent}      requirement_level: recommended`,
  ]
  if (span.activityLabel) {
    lines.push(`${indent}  note: "Activity label: ${span.activityLabel}"`)
  }
  if (span.parentSpanName) {
    lines.push(`${indent}  # parent: ${span.parentSpanName}`)
  }
  return lines.join('\n')
}

function buildYaml(processId, spec, spans) {
  const title = spec.metadata?.title ?? processId
  const spanYaml = spans.map(s => spanToYaml(s)).join('\n\n')

  return `# Auto-generated by powl-to-weaver.mjs — DO NOT EDIT
# Source: POWL AST for "${title}" (processId: ${processId})
# Generated: ${new Date().toISOString()}

groups:
  - id: registry.powl.${processId}
    type: attribute_group
    display_name: "POWL Process: ${title}"
    brief: "Auto-generated OTel span attributes for POWL process model '${title}'."
    stability: experimental
    attributes:
      - ref: powl.model.slug
      - ref: powl.model.fitness
      - ref: powl.iteration.count
      - ref: powl.model.operator_types
      - ref: powl.discovery.variant
      - ref: powl.threshold_met

${spanYaml}
`
}

// ── Main logic ──────────────────────────────────────────────────────────────

function processSingleAst(astPath, registryDir) {
  const spec = JSON.parse(readFileSync(astPath, 'utf-8'))
  const processId = spec.metadata?.processId
    ?? spec.metadata?.title?.toLowerCase().replace(/\s+/g, '-')
    ?? 'unknown-process'

  const spans = collectSpans(spec.root, processId, null)
  const yaml = buildYaml(processId, spec, spans)

  const outPath = join(registryDir, `powl-${processId}.yaml`)
  writeFileSync(outPath, yaml)
  console.log(`  Written: ${outPath} (${spans.length} spans)`)
  return outPath
}

function parseArgs(argv) {
  const args = {}
  for (let i = 0; i < argv.length; i++) {
    if (argv[i].startsWith('--')) {
      const key = argv[i].slice(2)
      args[key] = argv[i + 1] ?? true
      i++
    }
  }
  return args
}

const args = parseArgs(process.argv.slice(2))
const astPath = args['powl-ast'] ?? process.env.POWL_AST_PATH
const astDir  = args['powl-ast-dir']
const registryDir = resolve(args['registry'] ?? join(DIR, 'registry'))
const skipCheck = args['skip-check'] === 'true' || args['skip-check'] === true

mkdirSync(registryDir, { recursive: true })

if (astDir) {
  // Batch mode: process all ast.json files in subdirectories
  const dirEntries = readdirSync(astDir, { withFileTypes: true })
  for (const entry of dirEntries) {
    if (entry.isDirectory()) {
      const candidatePath = join(astDir, entry.name, 'ast.json')
      if (existsSync(candidatePath)) {
        processSingleAst(candidatePath, registryDir)
      }
    }
  }
} else if (astPath) {
  processSingleAst(resolve(astPath), registryDir)
} else {
  console.error('Usage: --powl-ast <path> | --powl-ast-dir <dir> | POWL_AST_PATH=<path>')
  process.exit(1)
}

// Run weaver registry check
if (!skipCheck) {
  const weaverBin = process.env.WEAVER_BIN ?? 'weaver'
  try {
    execSync(`${weaverBin} registry check --registry ${registryDir}`, {
      cwd: DIR,
      stdio: 'inherit',
    })
    console.log('weaver registry check: PASSED')
  } catch (err) {
    console.warn(`weaver registry check failed: ${err.message}`)
    console.warn('(Install weaver: cargo install weaver-forge)')
  }
}
