/**
 * @file SHACL-to-SPARQL-to-BEAM Compiler
 * @module atomvm/shacl-compiler
 */

import { createSPARQLPatternMatcher } from './sparql-pattern-matcher.mjs';

/**
 * Compiler that transforms SHACL shapes into BEAM bytecode via SPARQL
 */
export class SHACLCompiler {
  /**
   * @param {Object} options - Compiler options
   */
  constructor(options = {}) {
    this.options = options;
    this.matcher = createSPARQLPatternMatcher(null, {
      sh: 'http://www.w3.org/ns/shacl#',
      rdf: 'http://www.w3.org/1999/02/22-rdf-syntax-ns#',
      xsd: 'http://www.w3.org/2001/XMLSchema#',
    });
  }

  /**
   * Compile a SHACL shape to BEAM source code
   * @param {Object} shape - SHACL shape definition (simplified for POC)
   * @returns {string} BEAM source code
   */
  compile(shape) {
    const sparql = this.transpileToSPARQL(shape);
    return this.matcher.compileQueryToBeam(sparql);
  }

  /**
   * Transpile SHACL shape to SPARQL ASK query
   * @param {Object} shape - SHACL shape definition
   * @returns {string} SPARQL query
   */
  transpileToSPARQL(shape) {
    const { targetClass, properties } = shape;
    
    let whereClauses = [`?focusNode a <${targetClass}>`];
    let filters = [];

    if (properties) {
      properties.forEach((prop, index) => {
        const { path, datatype, minCount } = prop;
        const valVar = `?val${index}`;
        
        whereClauses.push(`?focusNode <${path}> ${valVar}`);
        
        if (datatype) {
          // This is a simplification: SHACL validation usually checks for violations
          // Here we are creating a query that matches VALID data
          filters.push(`DATATYPE(${valVar}) = <${datatype}>`);
        }
      });
    }

    const where = whereClauses.join(' . ');
    const filterStr = filters.length > 0 ? ` FILTER(${filters.join(' && ')})` : '';

    return `SELECT * WHERE { ${where}${filterStr} }`;
  }

  /**
   * Generate a full Erlang module for the shape
   * @param {string} moduleName - Name of the BEAM module
   * @param {Object} shape - SHACL shape
   * @returns {string} Erlang source code
   */
  generateErlangModule(moduleName, shape) {
    const beamCode = this.compile(shape);
    
    return `-module(${moduleName}).
-export([validate/1]).

%% @doc Validate a store against SHACL shape
%% Generated by SHACL-to-SPARQL-to-BEAM compiler
validate(Store) ->
    Results = ${beamCode},
    case Results of
        [] -> {error, validation_failed};
        _ -> ok
    end.
`;
  }
}

/**
 * Create a new SHACL compiler
 * @param {Object} options - Compiler options
 * @returns {SHACLCompiler} Compiler instance
 */
export function createSHACLCompiler(options) {
  return new SHACLCompiler(options);
}

export default SHACLCompiler;
